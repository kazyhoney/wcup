// Helper to get a random player, weighted by position for goal scoring
export const getScorerName = (teamId: string): string => {
    const players = TEAM_PLAYERS[teamId];
    if (!players || players.length === 0) return '選手';

    // Filter out GKs completely to ensure they never score
    const outfieldPlayers = players.filter(p => p.position !== 'GK');

    // If no outfield players found (shouldn't happen with correct data), fallback to any non-GK or generic
    if (outfieldPlayers.length === 0) return '選手';

    // Weights: FW: 60, WB: 15, MF: 20, DF: 5
    const weights: Record<string, number> = {
        'FW': 60,
        'WB': 15,
        'MF': 20,
        'DF': 5
    };

    const weightedList: string[] = [];
    outfieldPlayers.forEach(p => {
        const weight = weights[p.position] || 10;
        for (let i = 0; i < weight; i++) {
            weightedList.push(p.name);
        }
    });

    if (weightedList.length === 0) return outfieldPlayers[0].name;

    return weightedList[Math.floor(Math.random() * weightedList.length)];
};

// Fallback for simple name retrieval
export const getPlayerName = (teamId: string): string => {
    const players = TEAM_PLAYERS[teamId];
    if (players && players.length > 0) {
        return players[Math.floor(Math.random() * players.length)].name;
    }
    return '選手';
};

